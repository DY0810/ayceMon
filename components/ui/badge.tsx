import { mergeProps } from "@base-ui/react/merge-props"
import { useRender } from "@base-ui/react/use-render"
import { cva, type VariantProps } from "class-variance-authority"

import { cn } from "@/lib/utils"

const badgeVariants = cva(
  "group/badge inline-flex h-6 w-fit shrink-0 items-center justify-center gap-1 overflow-hidden rounded-full border border-transparent px-3 text-[11px] font-medium tracking-wide whitespace-nowrap transition-colors focus-visible:ring-2 focus-visible:ring-foreground/40 has-data-[icon=inline-end]:pr-2 has-data-[icon=inline-start]:pl-2 [&>svg]:pointer-events-none [&>svg]:size-3!",
  {
    variants: {
      variant: {
        default:
          "bg-foreground text-background [a]:hover:opacity-85",
        secondary:
          "bg-secondary text-secondary-foreground [a]:hover:opacity-85",
        destructive:
          "bg-destructive text-white [a]:hover:opacity-85",
        outline:
          "border border-foreground text-foreground bg-transparent [a]:hover:bg-secondary",
        ghost:
          "text-foreground hover:bg-secondary",
        link:
          "text-foreground underline-offset-4 hover:underline",
      },
    },
    defaultVariants: {
      variant: "default",
    },
  }
)

function Badge({
  className,
  variant = "default",
  render,
  ...props
}: useRender.ComponentProps<"span"> & VariantProps<typeof badgeVariants>) {
  return useRender({
    defaultTagName: "span",
    props: mergeProps<"span">(
      {
        className: cn(badgeVariants({ variant }), className),
      },
      props
    ),
    render,
    state: {
      slot: "badge",
      variant,
    },
  })
}

export { Badge, badgeVariants }
